using UnityEngine;
using System.Collections;

[RequireComponent(typeof(Rigidbody2D))]

public class Character2DTopDown : MonoBehaviour
{

    public float speed = 1.5f;
    public float acceleration = 100;
    public Animator animator;
    public float pushForce = 5.0f;

    private Vector3 direction;
    private Rigidbody2D body;

    void Start()
    {
        body = GetComponent<Rigidbody2D>();
        body.freezeRotation = true;
        body.gravityScale = 0;
    }

    void FixedUpdate()
    {
        body.AddForce(direction * body.mass * speed * acceleration);

        if (Mathf.Abs(body.linearVelocity.x) > speed)
        {
            body.linearVelocity = new Vector2(Mathf.Sign(body.linearVelocity.x) * speed, body.linearVelocity.y);
        }

        if (Mathf.Abs(body.linearVelocity.y) > speed)
        {
            body.linearVelocity = new Vector2(body.linearVelocity.x, Mathf.Sign(body.linearVelocity.y) * speed);
        }
    }


    void Update()
    {
        direction = new Vector2(Input.GetAxis("Horizontal"), Input.GetAxis("Vertical"));

        animator.SetFloat("speed", direction.sqrMagnitude);
        
    }
    void OnCollisionEnter2D(Collision2D collision)
    {
        // ?????????, ??????????? ?? ? ????????, ??????? ?????? ?????????? ??????
        if (collision.gameObject.CompareTag("Enemy"))
        {
            // ????????? ?????? ??????????? ?? ?????? ? ???????
            Vector2 pushDirection = (transform.position - collision.transform.position).normalized;

            // ????????????? ???? ???????????? (????? ????????????? ?????????)

            // ????????? ???? ????????????
            body.AddForce(pushDirection * pushForce, ForceMode2D.Impulse);
        }
    }
}